<!-- sentencia switch --> 

<?php 

    $i = 2;
 // se compara el valor ingresado si el valor ingresado es igual a alguno de los casos 
 // se devuelve un mensaje y se termina la sentencia con un "break"
 
    switch($i){
        case 0:
            echo "i es igual a 0";
        break;

        case 1:
            echo "i es igual a 1";
        break;

        case 2:
            echo "i es igual a 2";
        break;

        case 3: 
            echo "i es igual a 3";
        break;
    }

?>